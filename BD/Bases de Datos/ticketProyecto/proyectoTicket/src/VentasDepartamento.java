    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        String nombreSucursal;
        String nombreCategoria;
        String resultado =null;
        nombreCategoria=departamento.getSelectedItem().toString();
        nombreSucursal=sucursal.getSelectedItem().toString();
        try{
            Connection con = null;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection(url,usuario,contrase単a);
            CallableStatement proc=con.prepareCall("call ventaProductoSucursal(?,?,?)");
            proc.setString(1,nombreCategoria);
            proc.setString(2,nombreSucursal);
            proc.setString(3,"@totalVentas");
            proc.registerOutParameter("totalVentas", Types.FLOAT);
            proc.execute();
            resultado = proc.getString("totalVentas");
            total.setText("$ "+String.valueOf(resultado));
        }
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(altaSucursal.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_consultarActionPerformed

