    private void ConsultaEstados(){
            try{
            Connection con = null;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection(url,usuario,contrase침a);
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("SELECT nombreEstado FROM estado");
            estado.removeAllItems();
            while(rs.next()){
                estado.addItem(rs.getString(1));
            }
        }
        catch (ClassNotFoundException ex) {
           Logger.getLogger(altaSucursal.class.getName()).log(Level.SEVERE, null, ex);
       }
        catch (SQLException ex) {
           Logger.getLogger(altaSucursal.class.getName()).log(Level.SEVERE, null, ex);
       }

    }
